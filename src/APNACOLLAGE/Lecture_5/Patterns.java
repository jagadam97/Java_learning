package APNACOLLAGE.Lecture_5;

public class Patterns {
    public static void main(String[] args) {
        int m=5;
        int n=4;
        for(int i=0; i<m; i++){
            for(int j=0; j<n; j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
